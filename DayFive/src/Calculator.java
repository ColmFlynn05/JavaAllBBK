public class Calculator {

    public static double add(int numOne, int numTwo) {
        return numOne + numTwo;
    }

    public static double subtract(int numOne, int numTwo) {
        return numOne - numTwo;
    }

    public static double multiply(int numOne, int numTwo) {
        return numOne * numTwo;
    }

    public static double divide(int numOne, int numTwo) {
        return (double) numOne / numTwo;
    }

    public static double modulus(int numOne, int numTwo) {
        return numOne % numTwo;
    }

}
